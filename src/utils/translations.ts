// Translation helper function
export const translate = (key: string, params?: Record<string, unknown>, locale: string = 'en'): string => {
  const langTranslations = translations[locale as keyof typeof translations] || translations.en;
  let translation = langTranslations[key as keyof typeof langTranslations] || key;

  // Replace parameters
  if (params) {
    Object.keys(params).forEach(paramKey => {
      translation = translation.replace(`{${paramKey}}`, String(params[paramKey]));
    });
  }

  return translation;
};

// Export types for better type safety
export type SupportedLocale = keyof typeof translations;
export type TranslationKey = keyof typeof translations.en;
